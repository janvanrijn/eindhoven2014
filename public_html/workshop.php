      <h2>Workshop</h2>
      <p>OpenML is all about <a target="_blank" href="http://www.liacs.nl/~jvrijn/pdf/pub/sigkdd2014.pdf">Networked Machine Learning</a>, it offers researchers a new way of organizing results and building upon each others work. This workshop features two themes.</p>
      
      <h3>How to do it</h3>
      <p>The workshop features various talks about how to use OpenML in different settings. For details about the scientific talks provided during the workshop, see the <a href="prohram.php">program page</a>.</p>
      
      <h3>How to build upon it</h3>
      <p>The workshop is organized in a hackathon fashion, meaning that participants are encouraged to bring their laptops, and work together on various aspects that they need for their research. With the complete OpenML team available in Eindhoven, features that require server side programming can be created on the fly. As OpenML is an open source project, everyone who is interested is welcome to join.</p>
      
